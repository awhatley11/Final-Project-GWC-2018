function createTable (jsonDic, numColumns, rowClass, table) {
  var i = 0;
  var num = i;
  num.toString();

  while ( i < jsonDic.length) {
	var row = document.createElement("tr");
	row.className = rowClass

	// creates the row name and adds it to a global array so that it may be accessed by other functions
	document.getElementById(table).appendChild(row);

	for (var j = 0; j < numColumns; j++){
  	if (j < numColumns && i < jsonDic.length) {

    	var num = i;
    	num.toString();

    	var box = document.createElement("td");
    	box.className = "box";
    	boxName.push("box"+num);
    	box.id = ("box"+num);
    	//look at previous comment; same idea but for box/td instead
    	row.appendChild(box);

    	//appends link and name to the box
    	var nameText = document.createElement("p");
    	nameText.innerHTML = jsonDic[i].name;
    	nameText.className = "name";
    	box.appendChild(nameText);

    	var space = document.createElement("br");
    	box.appendChild(space);

    	var linkText = document.createElement("a");
    	linkText.href =  jsonDic[i].link;
    	linkText.innerHTML = "More Info";
    	linkText.className = "link";
    	box.appendChild(linkText);
      linkText.target = "_blank";
    	i++;
  	} else if (i == jsonDic.length) {
    	break
  	} else {
    	j = 0;
    	break;
  	}
	}
  }
}

