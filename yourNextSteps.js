var degreeDictionary = [];

function compileDegree() {
  for (var i = 0; i < database.length; i++) {
    var thing = database[i].prereq
    var search = thing.search("Degree")
    if (search != -1) {
      degreeDictionary.push(database[i])
    }
  }
}

var tableClassList = ["all","none","diploma", "degree"];

function hide(prereq) {
  for (var j = 0; j < tableClassList.length; j++) {
	if (tableClassList[j] != prereq ) {
  	var x = document.getElementById("table-" + tableClassList[j]);
    var w = document.getElementById("text-" + tableClassList[j])
  	x.style.display = "none";
    w.style.display = "none"
	} else {
  	var y = document.getElementById("table-" + prereq)
    var z = document.getElementById("text-" + prereq)
  	y.style.display = "table"
    z.style.display = "inline"
	}
  }
}


function noneDisplay() {
  hide("none");
}

function diplomaDisplay() {
  hide("diploma");
}

function allDisplay() {
  hide("all");
}

function degreeDisplay() {
  hide("degree")
}

